package com.Model;

import java.util.*;

public class Employee {

	int emp_id;
	String emp_name;
	String emp_designation;
	String gender;
	String emp_city;
	int emp_contact_no;
	String emp_email_id;
	Date emp_jointing_date;
	Date emp_date_of_birth;

	public int getEmp_id() {
		return emp_id;
	}
	public void setEmp_id(int emp_id) {
		this.emp_id = emp_id;
	}
	public String getEmp_name() {
		return emp_name;
	}
	public void setEmp_name(String emp_name) {
		this.emp_name = emp_name;
	}
	public String getEmp_designation() {
		return emp_designation;
	}
	public void setEmp_designation(String emp_designation) {
		this.emp_designation = emp_designation;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getEmp_city() {
		return emp_city;
	}
	public void setEmp_city(String emp_city) {
		this.emp_city = emp_city;
	}
	public int getEmp_contact_no() {
		return emp_contact_no;
	}
	public void setEmp_contact_no(int emp_contact_no) {
		this.emp_contact_no = emp_contact_no;
	}
	public String getEmp_email_id() {
		return emp_email_id;
	}
	public void setEmp_email_id(String emp_email_id) {
		this.emp_email_id = emp_email_id;
	}
	public Date getEmp_jointing_date() {
		return emp_jointing_date;
	}
	public void setEmp_jointing_date(Date emp_jointing_date) {
		this.emp_jointing_date = emp_jointing_date;
	}
	public Date getEmp_date_of_birth() {
		return emp_date_of_birth;
	}
	public void setEmp_date_of_birth(Date emp_date_of_birth) {
		this.emp_date_of_birth = emp_date_of_birth;
	}
	
	
}

